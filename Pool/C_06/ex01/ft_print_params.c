#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	x;
	int	y;

	y = 1;
	while (y < argc)
	{
		x = 0;
		while (argv[y][x])
		{
			write(1, &argv[y][x], 1);
			x++;
		}
		write(1, "\n", 1);
		y++;
	}
}
